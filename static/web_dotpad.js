canvas.height = arrayHeight * dotSize; // 캔버스 높이

function padArray(array, targetHeight, targetWidth) {
  const paddedArray = Array.from({ length: targetHeight }, () =>
    Array(targetWidth).fill(0)
  );

  for (let i = 0; i < array.length; i++) {
    for (let j = 0; j < array[i].length; j++) {
      paddedArray[i][j] = array[i][j];
    }
  }

  return paddedArray;
}

