//선아
function hexa(a) {
  let b = parseInt(a, 2);
  b = b.toString(16);
  return b;
}


function trans_hex_pad(a) {
  let trans_list = '';
  const J = [0, 4, 8, 12, 16, 20, 24, 28, 32, 36];
  for (let j of J) {

    let s = '';
    let ss = '';
    for (let i = 0; i < a[0].length - 1; i += 2) {
      s = '' + a[j + 3][i] + a[j + 2][i] + a[j + 1][i] + a[j][i];
      ss = '' + a[j + 3][i + 1] + a[j + 2][i + 1] + a[j + 1][i + 1] + a[j][i + 1];

      trans_list += hexa(ss) + hexa(s);
    }

  }
  return trans_list;
}





