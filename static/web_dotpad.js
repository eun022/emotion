function drawDotPattern(data) {
  // 캔버스 초기화
  ctx.clearRect(0, 0, canvas.width, canvas.height);

  // 배열에 따라 도트 그리기
  data.forEach((row, i) => {
    row.forEach((cell, j) => {
      if (cell === 1) {
        ctx.fillStyle = "black"; // 도트 색상 고정
        ctx.fillRect(
          j * dotSize, // x 좌표
          i * dotSize, // y 좌표
          dotSize - 2, // 도트 크기 (가로)
          dotSize - 2  // 도트 크기 (세로)
        );
      }
    });
  });
}

