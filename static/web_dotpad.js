//그래픽 패턴을 캔버스에 그리기 위한 함수
const canvas = document.getElementById("dotCanvas");
const ctx = canvas.getContext("2d");

function drawDotPattern(data) {
  ctx.clearRect(0, 0, canvas.width, canvas.height); //초기화
  const dotSize = 20; //도트 크기
  data.forEach((row, i) => {
    row.forEach((cell, j) => {
      if (cell === 1) {
        ctx.fillRect(j * dotSize, i * dotSize, dotSize - 2, dotSize - 2);
      }
    });
  });
}


//배열 가로로 합침
function mergeArraysHorizontally(...arrays) {
  //배열 길이 다름 -> ERROR
  const numRows = arrays[0].length;
  if (!arrays.every(arr => arr.length === numRows)) {
    throw new Error("배열의 행 수 틀림!!");
  }

  //각 행을 합쳐서 새로운 배열 생성
  const mergedArray = arrays[0].map((_, rowIndex) => {
    return arrays.reduce((acc, arr) => acc.concat(arr[rowIndex]), []);
  });

  return mergedArray;
}


