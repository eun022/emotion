// 배열 병합 함수
function mergeArrays(leftArray, centerArray, rightArray) {
  const mergedArray = Array.from({ length: 40 }, () =>
    Array(60).fill(0)
  );

  for (let row = 0; row < 40; row++) {
    for (let col = 0; col < 29; col++) {
      mergedArray[row][col] = leftArray[row][col];
    }
    for (let col = 0; col < 2; col++) {
      mergedArray[row][29 + col] = centerArray[row][col];
    }
    for (let col = 0; col < 29; col++) {
      mergedArray[row][31 + col] = rightArray[row][col];
    }
  }

  return mergedArray;
}




